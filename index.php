                        Oque é o SACC?
                    </h1>
                    <br>
                    <p>
                        Sacc é um sistema auxiliar de compra de Componentes que tem como objetivo facilitar ao usuário a
                        realizar uma boa compra de um computador. Este projeto é para as pessoas que não possuem
                        conhecimento algum na área da computação, serve como auxílio para estas pessoas realizarem uma
                        boa compra.
                        Sacc oferece informações relevantes e precisas sobre os Componentes disponíveis e que ajuda a
                        comparar e recomendar as melhores opções para cada necessidade ao cliente. O sistema será
                        intuitivo, de fácil utilização e capaz de fornecer informações precisas e atualizadas sobre a
                        tendência do mercado de Hardware.
                        O objetivo final é proporcionar aos usuários uma experiência de compra mais eficiente e
                        satisfatória, garantindo que eles obtenham o melhor desempenho e custo-benefício em seus
                        equipamentos de informática.

